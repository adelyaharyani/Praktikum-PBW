<?php
// Nilai awal
$a = 10;
$b = 3;

echo "Nilai a = $a <br>";
echo "Nilai b = $b <br><br>";

// Penjumlahan
$hasilTambah = $a + $b;
echo "Penjumlahan (a + b) = $hasilTambah <br>";

// Pengurangan
$hasilKurang = $a - $b;
echo "Pengurangan (a - b) = $hasilKurang <br>";

// Perkalian
$hasilKali = $a * $b;
echo "Perkalian (a * b) = $hasilKali <br>";

// Pembagian
$hasilBagi = $a / $b;
echo "Pembagian (a / b) = $hasilBagi <br>";

// Modulus
$hasilModulus = $a % $b;
echo "Modulus (a % b) = $hasilModulus <br><br>";

// Increment
$a++;
echo "Nilai a setelah Increment (a++) = $a <br>";

// Decrement
$b--;
echo "Nilai b setelah Decrement (b--) = $b <br>";
?>
